// Helper functions for filtering and searching projects
export const getProjectsByCategory = (category) => {
  const allProjects = [];
  
  Object.values(cseProjects.major).forEach(domain => {
    domain.projects.forEach(project => {
      if (project.category === category) {
        allProjects.push(project);
      }
    });
  });
  
  Object.values(cseProjects.minor).forEach(domain => {
    domain.projects.forEach(project => {
      if (project.category === category) {
        allProjects.push(project);
      }
    });
  });
  
  return allProjects;
};

export const getProjectsByTechnology = (technology) => {
  const allProjects = [];
  
  Object.values(cseProjects.major).forEach(domain => {
    domain.projects.forEach(project => {
      if (project.technologies.some(tech => tech.toLowerCase().includes(technology.toLowerCase()))) {
        allProjects.push(project);
      }
    });
  });
  
  Object.values(cseProjects.minor).forEach(domain => {
    domain.projects.forEach(project => {
      if (project.technologies.some(tech => tech.toLowerCase().includes(technology.toLowerCase()))) {
        allProjects.push(project);
      }
    });
  });
  
  return allProjects;
};

export const getAllProjects = () => {
  const allProjects = [];
  
  Object.values(cseProjects.major).forEach(domain => {
    allProjects.push(...domain.projects);
  });
  
  Object.values(cseProjects.minor).forEach(domain => {
    allProjects.push(...domain.projects);
  });
  
  return allProjects;
};

